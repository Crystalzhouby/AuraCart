{scenario_description}

## 对话历史
{recent_queries}

## 当前用户需求
{requirements}

## 已推荐商品信息（含商品基础信息 + 检索到的 FAQ/评价）
{retrieval_results}

## 检索失败的品类（避免生成这些品类的选项）
{failed_categories}
